package com.exclusive.ds.oops.polymorphism;

public class maths {
    public int add(int a) {
        return a;
    }

    public int add(int a, int b) {
        return a + b;

    }

    public int add(int a, int b, int c) {
        return a + b + c;
    }


    public int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }

}

